private void checkFields(){
        if(
                "".equals(txt_url.getText()) ||
                "".equals(txt_user.getText()) ||              
                "".equals(password = new String (pw_password.getPassword()))
                ){
            JOptionPane.showMessageDialog(null, "Debe llenar los campos marcados con *.");
        }
        else{            
            con = new MySQLConnection();
            url = txt_url.getText();
            ((MySQLConnection)con).setUrl(url);
            port = txt_port.getText();
            ((MySQLConnection)con).setPort(port);
            user = txt_user.getText();
            ((MySQLConnection)con).setUser(user);      
            password = new String (pw_password.getPassword());
            ((MySQLConnection)con).setPassword(password);  
            con.getConnection();
        }
    }
